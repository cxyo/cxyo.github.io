// 类型到类别代码的映射
const TYPE_TO_CATEGORY = {
  '大盘': 'B',
  '小盘': 'C',
  '策略': 'D',
  '行业': 'E',
  '主题': 'F',
  '海外': 'G',
  '债券': 'H'
};

// 生成 codeConfig 格式（用于兼容原有代码）
function generateCodeConfig() {
  const config = {
    'B': [], // 大盘
    'C': [], // 小盘
    'D': [], // 策略
    'E': [], // 行业
    'F': [], // 主题
    'G': [], // 海外
    'H': []  // 债券
  };
  
  FUND_CODE_CONFIG.forEach(item => {
    const category = TYPE_TO_CATEGORY[item.类型];
    if (category && !config[category].includes(item.指数代码)) {
      config[category].push(item.指数代码);
    }
  });
  
  return config;
}

// 生成 FUND_CODES_MAP 格式（用于兼容原有代码）
function generateFundCodesMap() {
  const map = {};
  
  FUND_CODE_CONFIG.forEach(item => {
    map[item.指数代码] = {
      '场内代码': item.场内代码,
      '场外代码A': item.场外代码A,
      '场外代码C': item.场外代码C
    };
  });
  
  return map;
}

// 生成 CATEGORY_MAP 格式（用于兼容原有代码）
function generateCategoryMap() {
  const map = {};
  
  FUND_CODE_CONFIG.forEach(item => {
    const category = TYPE_TO_CATEGORY[item.类型];
    if (category) {
      map[item.指数代码] = category;
    }
  });
  
  return map;
}

// 获取所有指数代码列表
function getAllIndexCodes() {
  return FUND_CODE_CONFIG.map(item => item.指数代码);
}

// 获取指数信息
function getIndexInfo(code) {
  return FUND_CODE_CONFIG.find(item => item.指数代码 === code);
}

